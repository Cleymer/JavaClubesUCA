        String consulta = "Select Count(*) as Asistencia from Alumno inner join AsistenciaAlumno on AsistenciaAlumno.alumno = Alumno.id "
                + "where asistenciaalumno.estado='Presente' and alumno.id="+id;
        
        Integer calculo=0, calc=0;
        
