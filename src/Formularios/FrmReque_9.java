    private void lstAlumnoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstAlumnoMouseClicked
        // TODO add your handling code here:
        String tmp = (String)lstAlumno.getSelectedValue();
        try {
            mostrarTxt(tmp);
        } catch (SQLException ex) {
            Logger.getLogger(FrmReque_9.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        evaluarAlumnoP(tmp);
        
        Integer numero = (Integer)prgPorcentaje.getValue();
        
        if(numero<70){
            txtAsunto.setEnabled(true);
            txtMensaje.setEnabled(true);
            btnEnviar.setEnabled(true);
            btnDefecto.setEnabled(true);

        }else{
            txtAsunto.setEnabled(false);
            txtMensaje.setEnabled(false);
            btnEnviar.setEnabled(false);
            btnDefecto.setEnabled(false);

        }
        
        if(numero<60){
            btnDes.setEnabled(true);
        }else{
            btnDes.setEnabled(false);
        }
        
    }//GEN-LAST:event_lstAlumnoMouseClicked

