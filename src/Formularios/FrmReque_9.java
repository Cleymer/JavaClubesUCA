    private void mostrarTxt(String id) throws SQLException{
        String busqueda = "Select a.id, a.nombre, a.apellidos, a.sexo, a.telefono, a.beca, a.tipobeca, a.email,"
                + "c.nombre as carrer, f.nombre as facultad from Alumno a, Carrera c, Facultad f "
                + "Where c.facultad=f.id and c.id=a.carrera and a.id="+id;
        ResultSet rs = null;
        Statement ps = null;
        Conexion conex = new Conexion();
        Connection conn = conex.obtenerConexion();
        
        
        
        try{
            ps = conn.createStatement(); 
            rs = conex.listarConsulta(busqueda);

            if(rs.next()){
                String add1 = rs.getString("id");
                txtID.setText(add1);
                String add2 = rs.getString("nombre");
                txtNombre.setText(add2);
                String add3 = rs.getString("carrer");
                txtCarrera.setText(add3);
                String add4 = rs.getString("facultad");
                txtFacultad.setText(add4);
                String add5 = rs.getString("apellidos");
                txtApellidos.setText(add5);
                String add6 = rs.getString("telefono");
                txtCel.setText(add6);
                String add7 = rs.getString("sexo");
                txtSexo.setText(add7);
                String add8 = rs.getString("beca");
                if(add8.equals("1")){
                    txtBeca.setText("Posee");
                }else{
                    txtBeca.setText("No posee");
                }

                String add9 = rs.getString("tipobeca");

                if(add9 == null){
                    txtTipo.setText("=======");

                }else{
                    txtTipo.setText(add9);
                }

                String add10= rs.getString("email");
                txtDestinatario.setText(add10);

            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }finally{
            try{
                ps.close();
                conex.cerrarConexion(conn);
                rs.close();
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }
    
