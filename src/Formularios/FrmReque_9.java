    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        // TODO add your handling code here:
        
        String n = (String)cmbClub.getSelectedItem();
        Alumno al = new Alumno();
        
        Properties propiedad = new Properties();
        propiedad.setProperty("mail.smtp.host","smtp.gmail.com");
        propiedad.setProperty("mail.smtp.starttls.enable","true");
        propiedad.setProperty("mail.smtp.port", "587");
        propiedad.setProperty("mail.stmp.auth", "true");
        
        Session sesion = Session.getDefaultInstance(propiedad);
        String correoEnvia = al.validarCorreoCoordinacion(n);
        String contrasena = al.validarPWDCoordinacion(n);
        String receptor = txtDestinatario.getText();
        String asunto = txtAsunto.getText();
        String mensaje=txtMensaje.getText();
        
        MimeMessage mail = new MimeMessage(sesion);
            try {
                mail.setFrom(new InternetAddress (correoEnvia));
                mail.addRecipient(Message.RecipientType.TO, new InternetAddress (receptor));
                mail.setSubject(asunto);
                mail.setText(mensaje);

                Transport transportar = sesion.getTransport("smtp");
                transportar.connect(correoEnvia,contrasena);
                transportar.sendMessage(mail, mail.getRecipients(Message.RecipientType.TO));          
                transportar.close();

                JOptionPane.showMessageDialog(null, "Listo, revise su correo");


            }catch(AddressException ex) {
                Logger.getLogger(Panel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (javax.mail.MessagingException ex) {
            Logger.getLogger(FrmReque_9.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        txtAsunto.setText("");
        txtMensaje.setText("");
        
    }//GEN-LAST:event_btnEnviarActionPerformed

